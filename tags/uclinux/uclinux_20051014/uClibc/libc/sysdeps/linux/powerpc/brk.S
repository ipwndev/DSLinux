#include "ppc_asm.h"
#define _ERRNO_H	1
#include <bits/errno.h>
#include <sys/syscall.h>

#ifdef __NR_brk
	.comm	__curbrk,4,4
	.section ".text"
.globl      __brk;
.type      __brk, @function;
.align  2;

__brk:
	stwu    r1,-16(r1)
	stw	r3,8(r1)
	li 0, __NR_brk;   
	sc
	lwz     r6,8(r1)
#ifdef __PIC__
	mflr    r4
	bl      _GLOBAL_OFFSET_TABLE_@local-4
	mflr    r5
	lwz     r5,__curbrk@got(r5)
	mtlr    r4
	stw     r3,0(r5)
#else
	lis     r4,__curbrk@ha
	stw     r3,__curbrk@l(r4)
#endif
	cmplw   r6,r3
	addi    r1,r1,16
	li	r3,0
	blelr+
	li      r3,ENOMEM
#ifdef __PIC__
	b	__syscall_error@plt
#else
	b	__syscall_error
#endif
.size __brk,.-__brk

.weak brk;
brk=__brk        
#endif	
