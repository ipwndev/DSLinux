#include <string.h>

#include "i_system.h"

int		myargc;
char**		myargv;




//
// M_CheckParm
// Checks for the given parameter
// in the program's command line arguments.
// Returns the argument number (1 to argc-1)
// or 0 if not present
int M_CheckParm (char *check)
{
    int		i;

    for (i = 1;i<myargc;i++)
    {
	if ( !I_strncasecmp(check, myargv[i], strlen(check)) )
	    return i;
    }

    return 0;
}




